#import "UISearchBar+RAC.h"
#import <objc/objc-runtime.h>

@interface UISearchBar () <UISearchBarDelegate>

@end

@implementation UISearchBar (RAC)
- (RACSignal *)rac_searchBarShouldBeginEditingSignal
{
    self.delegate = self;
    RACSignal *signal = objc_getAssociatedObject(self, _cmd);
    if (signal != nil) {
        return signal;
    }
    
    signal = [[self rac_signalForSelector:@selector(searchBarShouldBeginEditing:) fromProtocol:@protocol(UISearchBarDelegate)] map:^id(RACTuple *tuple) {
        return tuple.first;
    }];
    
    objc_setAssociatedObject(self, _cmd, signal, OBJC_ASSOCIATION_RETAIN_NONATOMIC);
    return signal;
}

